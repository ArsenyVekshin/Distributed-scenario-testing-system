package com.disttest.coordinator.model

import com.fasterxml.jackson.annotation.JsonAlias

/**
 * Корневые DTOs для десериализации YAML-конфигов сценария.
 *
 * Соответствуют спецификации языка в docs/SCENARIO_CONFIG.md.
 * Поля намеренно не содержат бизнес-логики — только структура данных.
 */

// ─── Корневой объект ───────────────────────────────────────────────────────────

data class ScenarioConfig(

    /** Версия формата конфига. Единственное поддерживаемое значение: "1". */
    val version: String = "1",

    /** Название сценария. Обязательное, не может быть пустым. */
    val name: String,

    /** Описание сценария. Опционально. */
    val description: String = "",

    /**
     * Максимальное время выполнения всего сценария в секундах.
     * По умолчанию 86400 (24 часа). 0 — без ограничений.
     */
    val timeout: Int = 86_400,

    /**
     * Переменные окружения уровня сценария.
     * Наследуются всеми блоками и тестами; могут быть переопределены на нижних уровнях.
     */
    val env: Map<String, String> = emptyMap(),

    /** Список блоков выполнения. Минимум 1. */
    val blocks: List<BlockConfig> = emptyList(),
)

// ─── Блок ─────────────────────────────────────────────────────────────────────

data class BlockConfig(

    /** Уникальный идентификатор блока внутри сценария. Обязательное. */
    val id: String,

    /** Отображаемое имя блока. Если не указано — равно [id]. */
    val name: String = "",

    /**
     * Количество параллельных потоков для запуска тестов внутри блока.
     * Минимум 1. По умолчанию 1.
     */
    val parallelism: Int = 1,

    /**
     * Максимальное время выполнения блока целиком в секундах.
     * 0 — без ограничений на уровне блока (ограничен таймаутом сценария).
     */
    val timeout: Int = 0,

    /**
     * Количество повторных попыток блока при неуспешном завершении.
     * В MVP не используется для автоматического retry на уровне блока,
     * но поле сохраняется в БД и передаётся агенту. 0 — не повторять.
     */
    val retries: Int = 0,

    /**
     * ID блоков, которые должны завершиться успешно перед запуском этого блока.
     * Формирует рёбра DAG. Каждый ID должен ссылаться на существующий блок.
     */
    @JsonAlias("depends_on")
    val dependsOn: List<String> = emptyList(),

    /**
     * Метки блока для фильтрации и отчётов.
     * Например: `[smoke, regression, auth]`
     */
    val tags: List<String> = emptyList(),

    /**
     * Переменные окружения уровня блока.
     * Мерджатся поверх глобального env сценария;
     * могут быть переопределены на уровне теста.
     */
    val env: Map<String, String> = emptyMap(),

    /** Список тестов. Минимум 1. */
    val tests: List<TestCaseConfig> = emptyList(),
)

// ─── Тест ─────────────────────────────────────────────────────────────────────

data class TestCaseConfig(

    /** Уникальный идентификатор теста внутри блока. Обязательное. */
    val id: String,

    /** Отображаемое имя теста. Если не указано — равно [id]. */
    val name: String = "",

    /** Команда для выполнения на агенте. Обязательное, не может быть пустой. */
    val command: String,

    /**
     * Таймаут теста в секундах. По умолчанию 60.
     * Поддерживаются оба написания для обратной совместимости.
     */
    @JsonAlias("timeoutSeconds", "timeout_seconds")
    val timeout: Int = 60,

    /**
     * Количество повторных попыток при неуспешном завершении (exit code != 0).
     * 0 — не повторять. По умолчанию 0.
     */
    val retries: Int = 0,

    /**
     * Рабочая директория для выполнения команды на агенте.
     * Пустая строка — директория по умолчанию агента.
     */
    val workdir: String = "",

    /**
     * Метки теста для фильтрации и отчётов.
     * Например: `[unit, fast, auth]`
     */
    val tags: List<String> = emptyList(),

    /**
     * Переменные окружения теста.
     * Мерджатся поверх env блока и сценария (наивысший приоритет).
     */
    val env: Map<String, String> = emptyMap(),
)
